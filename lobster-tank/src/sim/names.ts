const fallbackName = (rng: RandomFn, index: number) =>
  `Lobster-${Math.floor(rng() * 9999)}-${index}`;

export const pickRandomName = (
  rng: RandomFn,
  used?: Set<string>,
  index = 0,
): string => {
  if (LOBSTER_NAMES.length === 0) return fallbackName(rng, index);
  let attempts = 0;
  while (attempts < 10) {
    const name = LOBSTER_NAMES[Math.floor(rng() * LOBSTER_NAMES.length)];
    if (!used || !used.has(name)) return name;
    attempts += 1;
  }
  return fallbackName(rng, index);
};
